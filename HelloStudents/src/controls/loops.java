package controls;
public class loops
{
	public static void main(String[] args)
	{
		
// while
		int count = 1;
		while(count  < 11)
		{
			System.out.println("Count is:" + count);
			count++;
		}
		
// do while		
		count = 1;
		do
		{
			System.out.println("Count is:" + count);
			count++;
		}
		while(count < 11);
		
// for
		for (int i = 1; i < 11; i++)
			System.out.println("Count is: " + count);
	}
}